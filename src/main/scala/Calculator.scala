object Calculator extends App {
  def add(x: Int, y: Int) = {
    x + y
  }
  def sub(x: Int, y: Int) : Int = {
    x - y
  }
  def mul(x: Int, y: Int) : Int = {
    x * y
  }
  def div(x: Int, y: Int) : Int = {
    x / y
  }
  def square(x: Int) : Int = {
    x * x
  }
  def cube(x: Int) : Int = {
    x * x * x
  }
}